import { sequelize } from '../sequelize';
const Sequelize = require('sequelize');

export const Task = sequelize.define('task', {
  id: {
    type: Sequelize.INTEGER,
    primaryKey: true
  },
  listId: {
    type: Sequelize.INTEGER
  },
  name: {
    type: Sequelize.STRING
  },
  isFinished: {
    type: Sequelize.BOOLEAN
  }  
});
